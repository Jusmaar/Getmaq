    getOrdenAlfabetico() {
        const CANTITAD = this.eqCategorias.length;
        let array = (this.eqCategorias.slice(0, CANTITAD - 1)).sort(function (a, b) {
            if (a.nombre.toLowerCase() < b.nombre.toLowerCase()) return -1;
            if (a.nombre.toLowerCase() > b.nombre.toLowerCase()) return 1;
            return 0;
        });
        array[CANTITAD - 1] = this.eqCategorias[CANTITAD - 1];
        return array;
    }
    getNameCategoria(cod) {
        let j = 0;
        let nombrecate: string;
        for (let i in this.eqCategorias) {
            if ((this.eqCategorias[i]['categoria'] == cod)) {
                nombrecate = this.eqCategorias[i]['nombre'];
                j++;
            }
        }
        //  console.log( this.nombrecate );
        return nombrecate;
    }

    getNameSubcategoria(codc, cods) {
        let j = 0;
        let nombresubca: string;
        for (let i in this.eqCategorias) {
            if ((this.eqCategorias[i]['categoria'] == codc) && (this.eqCategorias[i]['subcategoria'] === cods)) {
                nombresubca = this.eqCategorias[i]['nombre'];
                j++;
            }
        }
        return nombresubca;
    }

    getUrlLogo(codCategoria) {
        // codCategoria = "02"
        for (let j = 0; j < this.listCategoria.length; j++) {
            if (codCategoria == this.listCategoria[j].value) {
                return this.listCategoria[j].icon;
            }
        }
        return '';
    }

