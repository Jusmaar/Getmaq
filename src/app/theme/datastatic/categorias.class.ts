        // categorias = "00_02_03_00_00_00_00_0_1_5_0_0_0_0"
        let listaImagenesCat: string[] = [];
        let aux: any = ''
        let strLocalCategorias = '';
        strLocalCategorias = categorias.split('_');
        for (let k = 0; k < 7; k++) {
            aux = this.getUrlLogo(strLocalCategorias[k]);
            if (aux != '') {
                listaImagenesCat.push(aux);
            }
        }
        return listaImagenesCat;
